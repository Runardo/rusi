    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int linha = tbReserva.getSelectedRow();
        if (linha >= 0) {
            if (JOptionPane.showConfirmDialog(this, "Deseja excluir o registro?") == 0) {
                if (controle.excluir(model.getReserva(linha))) {
                    JOptionPane.showMessageDialog(this, controle.getMsg());
                    model.removerReserva(linha);
                } else {
                    JOptionPane.showMessageDialog(this, controle.getMsg());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um registro! >:(");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int linha = tbReserva.getSelectedRow();
        if (linha >= 0) {
            EdicaoReserva edicaoReserva = new EdicaoReserva(null, true);
            controle.setReserva(model.getReserva(linha));
            edicaoReserva.carregarCampos(model.getReserva(linha));
            edicaoReserva.setVisible(true);
            model.atualizarReserva(linha);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um registro! >:(");
        }
    }//GEN-LAST:event_btEditarActionPerformed

